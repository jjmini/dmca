package com.glority.quality.sca.mi.report;

import com.glority.quality.sca.mi.metrics.FunctionMetrics;

/**
 * add filePath to FunctionMetrics.
 * 
 * @author LYX
 */
public final class FunctionMetricsAdd {
    private FunctionMetrics fm;
    private String filePath;

    public FunctionMetricsAdd() {
        fm = new FunctionMetrics();
    }

    public FunctionMetricsAdd(FunctionMetrics fm, String filePath) {
        this.fm = fm;
        this.filePath = filePath;
    }

    public FunctionMetrics getFM() {
        return fm;
    }

    public String getFilePath() {
        return filePath;
    }
}
