package com.glority.qualityserver.metricengine;

import java.util.List;

import com.glority.qualityserver.web.model.MetricScoreFormula;

/**
 * Interface to generate MetricFormula.
 * 
 */
public interface MetricFormulaGenerator {

    /**
     * Generate the Metric Score Formula using metric name and list of
     * ExtendItem.
     * 
     * @param metricName
     *            metric name
     * @param itemList
     *            ExtendItem list
     * @return MetricScoreFormula instance
     */
    MetricScoreFormula generateFormula(String metricName, List<ExtendItem> itemList);

}
